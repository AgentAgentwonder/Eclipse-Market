export const defaultThemePreset = themePresets[1];

export const DEFAULT_THEME_ID = defaultThemePreset.id;

export const createThemeFromPreset = (presetId: string) => {
  const basePreset = themePresets.find(preset => preset.id === presetId) ?? defaultThemePreset;
  return {
    id: `theme-${presetId}`,
    name: basePreset.name,
    colors: basePreset.colors,
    effects: basePreset.effects ?? defaultEffects,
    isCustom: false,
    createdAt: baseTimestamp,
    updatedAt: baseTimestamp,
  };
};
